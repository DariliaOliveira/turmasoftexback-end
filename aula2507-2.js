dia = 5;
nomedia = "";

switch(dia) {
    case 1:
        nomedia = "Domingo";
        break;
    case 2:
        nomedia = "Segunda";
        break;
    case 3:
        nomedia = "Terça";
        break;
    case 4:
        nomedia = "Quarta";
        break;
    case 5:
        nomedia = "Quinta";
        break;
    case 6:
        nomedia = "Sexta";
        break;
    case 7:
        nomedia = "Sábado";
        break;
    default:
        nomedia = "Dia inexistente";
}
console.log(nomedia);




